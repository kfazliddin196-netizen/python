from tkinter import *
root = Tk()
root.title("METANIT.COM")
root.geometry("500x500")
canvas = Canvas(bg="white", width=500, height=500)
canvas.pack( expand=1)
canvas.create_rectangle(350, 350, 140, 150, fill="#80CBC4", outline="#004D40")
p = PhotoImage(file="img.png", width=180, height=190)
canvas.create_image(250, 250, image=p)
root.mainloop()